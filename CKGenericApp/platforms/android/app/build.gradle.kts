// Function to get git commit count
fun getGitCommitCount(): Int {
    return try {
        val output = ProcessBuilder("git", "rev-list", "--count", "HEAD")
            .directory(rootDir)
            .redirectErrorStream(true)
            .start()
            .inputStream
            .bufferedReader()
            .readText()
            .trim()
        output.toIntOrNull() ?: 1
    } catch (e: Exception) {
        1
    }
}

